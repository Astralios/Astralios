void iso_font_draw(const font_t *font, const char c, uint32_t color, fb_driver_t *fb_driver, size_t x, size_t y)
{
    const uint8_t *chars = (const uint8_t*)font->chars;
    for (size_t i = 0; i < font->height; i++)
    {
        for (size_t j = 0; j < font->width; j++)
        {
            if (chars[c * font->height + i] & (1 << (font->width - 1 - j)))
            {
                fb_driver_set_pixel(fb_driver, j + x, i + y, color);
            }
        }
    }
}

